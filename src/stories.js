你把灯调低一点。房间里的影子变得柔和，木梁的边缘退进暗处，窗外的雨痕反而清楚了一点。桌上的陶杯已经不那么热了，杯底还留着一点淡淡的温意。面包只吃了一小块，剩下的放在盘子里，像可以留给明天早晨。书页停在一段没有读完的地方，夹一张纸也好，不夹也好，反正书不会走，故事也不会走。明天醒来，它还会在这里。

阿尔萨斯的雨夜很适合让人放下白天的路线。那些小镇名字，那些桥，那些教堂，那些葡萄园和河边步道，都可以暂时退到窗外去。它们不需要在今晚被安排得很清楚。旅行有时候也可以只是这样：住进一间老木屋的阁楼，听一场不大的雨，看街灯照在湿石路上，让一杯热饮慢慢变凉，让身体一点一点知道，今天已经结束了。

睡意就是从这场雨里来的。它先落在窗台的花叶上，又落在鹅卵石的小巷里，再沿着屋檐、木梁、灯光和壁炉的暖意，慢慢来到你的眼前。你看见对面窗里的灯变得模糊，看见街灯在雨水里晕成一团金色，看见木桌上的书页像白色的小船，轻轻停在夜里。你的肩膀松下来，手指也不再想去翻下一页。雨还在下，可它已经不属于外面的天气了，它变成了屋子的一部分，变成了睡意的一部分。

[壁炉声渐轻]

