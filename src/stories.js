远处教堂的钟声响了一下。声音不重，隔着雨和屋顶传来，已经变得很柔。它从小镇另一头慢慢过来，经过湿亮的广场，经过关了门的面包房，经过桥边的栏杆，最后落在你的窗前。钟声过后，雨声显得更清楚。屋檐下的水一滴一滴落进石槽里，巷子里的水沿着坡度缓缓流动，窗台上的花叶承着雨，偶尔轻轻颤一下，抖落一串细小的水珠。

[远处钟声]

你走到窗边，把玻璃上的雾气轻轻擦开一小块。外面的街灯正照着一段弯弯的小巷，巷子尽头有一扇绿色木门，门前放着两个花盆。雨水落在门阶上，泛出一点亮。再远一点，屋顶一层一层向后退去，斜斜的，深深浅浅地连在一起。小镇并不大，可夜里看起来像藏着许多安静的房间，许多还没有讲完的故事。每一扇亮着的窗后面，都有一个自己的夜晚；每一扇暗下去的窗后面，都有人已经睡了。

你不必猜他们是谁。睡前的小镇不需要故事太清楚。它只需要有一些灯，一些雨，一些木头，一些石头，一条没有人赶路的小巷。白天那些漂亮的门楣、花窗、招牌和桥影，到了夜里都收起了颜色，变得更像梦。雨水让它们贴近了，也让它们离人更远。你站在窗边看一会儿，便会觉得自己并不是一个匆匆经过的旅人，而像被这间老屋、这条巷子和这场雨轻轻留住了一晚。

[木窗轻轻响了一下]

