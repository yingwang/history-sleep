楼下传来很轻的脚步声，也许是旅馆主人在检查门锁。脚步经过楼梯口，又慢慢远了。老木楼梯发出两三声轻响，不刺耳，反而让人安心。这样的房子总是会响的，风来时响，雨来时响，有人走过时响，火在炉里低低烧着时也响。它不像现代房间那样完全安静，却正因为有这些细小的声音，夜才显得有边界，有温度，有人在远处照看。

