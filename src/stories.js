现在仍是阿尔萨斯木筋屋的雨夜。灯低低地亮着，壁炉还留着一点暖，窗外的小巷空着，雨水慢慢落在屋瓦、花叶、石阶和深色木梁之间。老屋没有说话，只用它的木头、墙、窗、火光和细小声响，把这一晚安稳地托住。

